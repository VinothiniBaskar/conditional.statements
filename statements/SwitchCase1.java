package conditional.statements;

public class SwitchCase1 {

	public static void main(String[] args) {
		int number = 11;
		switch (number) {
		case 0:
			System.out.println("0");
			break;
		case 1:
			System.out.println("you have entered as 1");

			break;
		case 2:
			System.out.println("you have entered as 2");

			break;
		case 3:
			System.out.println("you have entered as 3");

			break;
		case 4:
			System.out.println("you have entered as 4");

			break;
		case 5:
			System.out.println("you have entered as 5");

			break;
		case 6:
			System.out.println("you have entered as 6");

			break;
		case 7:
			System.out.println("you have entered as 7");

			break;
		case 8:
			System.out.println("you have entered as 8");

			break;
		case 9:
			System.out.println("you have entered as 9");

			break;
		default:
			System.out.println("number is not there in this case");
			break;
		}

	}

}
